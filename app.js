function conferirRato() {
  var respR = document.getElementById("respR").value;
  const respT = document.getElementById("respT").value

  if (respR === "r" && respT === "t") // verifa se os dois são verdadeiros
  {
    document.getElementById("respostaRato").innerHTML = "Parabéns! Você acertou";
  }
  else {
    document.getElementById("respostaRato").innerHTML = "Resposta Errada, tente novamente!";
  }
}
